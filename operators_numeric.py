#Operators Numeric Data

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%) show us the remainder in a calculation 
# Exponent (**)

x = 10
y = 2 

print(x+y) #addition 
print(x-y) #subtraction 
print(x*y) #multiplication
print(x/y) #division
print(x%y) #modulus 
print(5%2) #modulus 
print(x**y) #exponent 10 to the power of 2 

